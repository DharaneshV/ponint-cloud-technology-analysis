def extract_single_scan_features(pcd):
    """Extracts scale-invariant shape and density features from a single truck cluster."""
    points = np.asarray(pcd.points)
    if len(points) < 100:
        return None
        
    z_coords = points[:, 2]
    
    # Floor estimation: since Z points down, the floor is the highest Z values.
    # We use the 95th percentile to ignore noise at the very bottom.
    floor_z = np.percentile(z_coords, 95)
    
    # 1. Z-depth statistics
    mean_z = np.mean(z_coords)
    median_z = np.median(z_coords)
    std_z = np.std(z_coords)
    skew_z = skew(z_coords)
    
    # 2. Coverage proxy (percentage of points that are > 10cm above the floor)
    # Since lower Z means higher physically:
    cargo_points = np.sum(z_coords < (floor_z - 10.0))  # 10.0 cm
    coverage_proxy = cargo_points / len(points)
    
    # 3. Density
    aabb = pcd.get_axis_aligned_bounding_box()
    extent = aabb.get_extent() # [x, y, z] in cm
    vol_cm3 = extent[0] * extent[1] * extent[2]
    density = len(points) / vol_cm3 if vol_cm3 > 0 else 0
    
    return {
        'mean_z': mean_z,
        'median_z': median_z,
        'std_z': std_z,
        'skew_z': skew_z,
        'coverage_proxy': coverage_proxy,
        'point_density': density
    }

def process_single_file(filepath):
    print(f"\nProcessing {os.path.basename(filepath)}...")
    
    # Use io_utils which natively handles the sky code bug
    pcd = io_utils.read_point_cloud(filepath)
    
    pcd = noise_filter.remove_statistical_outliers(pcd)
    pcd = gantry_filter.filter_gantry(pcd)
    
    # --- EXPLICIT GROUND FILTERING CHECK (User Feedback #2) ---
    points = np.asarray(pcd.points)
    if len(points) == 0:
        return None
        
    ground_z = np.max(points[:, 2]) # Z points downwards
    
    # In cm, floor is usually at ~587 cm. We check if ground_z is sane.
    if abs(ground_z - 587.0) > 50.0:
        print(f"  WARNING: Ground Z ({ground_z:.1f}cm) differs wildly from expected 587.0cm. Skipping to avoid calibration risk.")
        return None
        
    pcd = ground_plane_removal.remove_ground_plane(pcd)
    pcd = downsample.downsample_point_cloud(pcd)
    
    labels = clustering.cluster_points(pcd)
    truck_pcd = truck_extraction.extract_truck_cluster(pcd, labels)
    
    return truck_pcd

def main():
    empty_dir = r"d:\point cloud technology\bgtest-3\empty"
    load_dir = r"d:\point cloud technology\bgtest-3\load"
    
    out_csv = r"d:\point cloud technology\Truck-PointCloud\ml_model\independent_dataset.csv"
    reference_csv = r"d:\point cloud technology\Truck-PointCloud\ml_model\sick_validation_dataset.csv"
    
    # 1. Load the ground-truth labels generated by the geometric pipeline
    ref_df = pd.read_csv(reference_csv)
    label_map = dict(zip(ref_df['filename'], ref_df['geometric_label']))
    fill_map = dict(zip(ref_df['filename'], ref_df['fill_percentage']))
    
    # 2. Glob exactly SL1 files
    empty_files = sorted(glob.glob(os.path.join(empty_dir, "SL1*_RD.txt")))
    load_files = sorted(glob.glob(os.path.join(load_dir, "SL1*_RD.txt")))
    
    all_files = empty_files + load_files
    
    # Explicit assertion: Ensure NO SL2 files snuck in
    for f in all_files:
        assert "SL1" in os.path.basename(f), f"Non-SL1 file detected: {f}"
        
    dataset = []
    
    for filepath in all_files:
        filename = os.path.basename(filepath)
        
        if filename not in label_map:
            print(f"Skipping {filename} - no ground-truth label from geometric pipeline.")
            continue
            
        truck_pcd = process_single_file(filepath)
        if truck_pcd is None:
            print(f"Skipping {filename} - point cloud extraction failed.")
            continue
            
        feats = extract_single_scan_features(truck_pcd)
        if feats is not None:
            feats['filename'] = filename
            feats['geometric_label'] = label_map[filename]
            fill_pct = fill_map[filename]
            feats['target_volume_m3'] = (fill_pct / 100.0) * 16.8814
            dataset.append(feats)
            print(f"  Extracted: {feats}")
            
    df = pd.DataFrame(dataset)
    df.to_csv(out_csv, index=False)
    print(f"\nSaved {len(df)} independent scan features to {out_csv}")

if __name__ == "__main__":
    main()
